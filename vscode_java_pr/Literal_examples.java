import java.util.Scanner;
public class Literal_examples{
    public static void main(String args[]){
        int a=123;
        System.out.println(a);
        // 0b ->here can give the output in binary literal
        int b=0b1100;
        System.out.println(b); //out:12
       // 0x -> hexadecimal
        int c=0xA;
        System.out.println(c);
       //0-> ocatal
        int d=0123;
        System.out.print(d); //out:
        
    }
}